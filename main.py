import math
import sys

#дана площа


print("Введіть площу описаного квадрата")
Sb = input()

if float(Sb) <= 0:
    print("Площина не може бути менше або рівне за 0")
    sys.exit()

a = math.sqrt(float(Sb))            #сторона описаного квадрата і діагональ вписаного

Ss = 0.5 * float(a) ** 2
print("Площа вписаного квадрата дорівнює ", Ss)                 #Відповідь
k = float(Sb)/float(Ss)
print("Площа вписаного квадрата у ", k , "разів менша за площу описаного квадрата")
